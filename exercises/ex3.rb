# Naming some math characters
#  +   plus
#  -   minus
#  /   slash
#  *   asterisk
#  %   percent
#  <   less-than
#  >   greater-than
#  <=  less-than-equal
#  =>  greater-than-equal

puts "I will now count my chickens:" #Statement for what we're doing with the code

puts "Hens #{25.0 +30.0 / 6.0}" #Arbitrary math to count the hens
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}" #Arbitrary math to count the Roosters

puts "Now I will count the eggs:" #Statement to count the eggs

puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0 #Egg counting math. Specifically a combo of multiple functions we named above.

puts "Is it true that 3 + 2 < 5 - 7?" #Statement to be answered w/ math

puts 3.0 + 2.0 < 5.0 - 7.0 #Math to determine a true or false statement with the `<` comparator

puts "What is 3 + 2? #{3.0 + 2.0}" #Why false? Do both maths.
puts "What is 5 - 7? #{5.0 - 7.0}" #Why false? Do both maths. 

puts "Oh, that's why it's false." #Self actualize and come to terms with our false statement.

puts "How about some more." #Having self actualized, we must continue maths.

puts "Is it greater? #{5.0 > -2.0}" #Comparing the output values of the previous maths with greater-than
puts "Is it greater or equal? #{5.0 >= -2.0}" #Comparing the output values of the previous maths with greater-than-equal
puts "Is it less or equal? #{5.0 <= -2.0}" #Comparing the output values of the previous maths with less-than-equal